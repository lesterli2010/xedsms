package com.hxdq.xedsms.db.model;

import com.hxdq.xedsms.db.Entity;

public class City extends Entity {
    private Integer cityId;

    private String cityName;

    private String cityZcode;

    private String cityPcode;

    private Integer provId;

    private String cityPy;

    private String cityZm;

    public Integer getCityId() {
        return cityId;
    }

    public void setCityId(Integer cityId) {
        this.cityId = cityId;
    }

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getCityZcode() {
        return cityZcode;
    }

    public void setCityZcode(String cityZcode) {
        this.cityZcode = cityZcode;
    }

    public String getCityPcode() {
        return cityPcode;
    }

    public void setCityPcode(String cityPcode) {
        this.cityPcode = cityPcode;
    }

    public Integer getProvId() {
        return provId;
    }

    public void setProvId(Integer provId) {
        this.provId = provId;
    }

    public String getCityPy() {
        return cityPy;
    }

    public void setCityPy(String cityPy) {
        this.cityPy = cityPy;
    }

    public String getCityZm() {
        return cityZm;
    }

    public void setCityZm(String cityZm) {
        this.cityZm = cityZm;
    }
}