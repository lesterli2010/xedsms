package com.hxdq.xedsms.web.common;

public class PageCompProp {
	private String _id;
	private String _att_name;
	private String _att_value;
	private String _type;
	
	public PageCompProp(String id, String attName, String type, String attValue){
		this._id = id;
		this._att_name = attName;
		this._att_value = attValue;
		this._type = type;
	}
	
	public String getID(){
		return this._id;
	}
	
	public String getAttName(){
		return this._att_name;
	}
	
	public String getAttValue(){
		return this._att_value;
	}
	
	public String getType(){
		return this._type;
	}


}
